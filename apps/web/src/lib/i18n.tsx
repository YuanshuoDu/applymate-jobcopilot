// ── Context ──────────────────────────────────────────────────────────────────

interface I18nContextValue {
  lang:    Lang
  t:       (key: string) => string
  setLang: (l: Lang) => void
}

const I18nContext = createContext<I18nContextValue>({
  lang:    'en',
  t:       (key) => key,
  setLang: () => {},
})

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = useState<Lang>(() => {
    if (typeof window !== 'undefined') {
      const stored = localStorage.getItem('applymate_lang') as Lang | null
      if (stored && stored in dict) return stored
      // Auto-detect from browser
      const browser = navigator.language?.slice(0, 2).toLowerCase()
      if (browser === 'de') return 'de'
      if (browser === 'fr') return 'fr'
      if (browser === 'es') return 'es'
      if (browser === 'nl') return 'nl'
      if (browser === 'zh') return 'zh'
    }
    return 'en'
  })

  const setLang = useCallback((l: Lang) => {
    setLangState(l)
    try { localStorage.setItem('applymate_lang', l) } catch {}
  }, [])

  const t = useCallback((key: string): string => {
    return dict[lang]?.[key] ?? dict.en?.[key] ?? key
  }, [lang])

  return React.createElement(I18nContext.Provider, { value: { lang, t, setLang } }, children)
}

export function useI18n() {
  return useContext(I18nContext)
}

